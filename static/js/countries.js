// Function to populate country datalist
function populateCountryList(datalistId) {
    const datalist = document.getElementById(datalistId);
    if (!datalist) return;
    
    datalist.innerHTML = '';
    COUNTRIES_DATA.forEach(country => {
        const option = document.createElement('option');
        option.value = country.dial;
        option.textContent = `${country.flag} ${country.name} ${country.dial}`;
        if (country.default) {
            option.selected = true;
        }
        datalist.appendChild(option);
    });
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', function() {
    populateCountryList('country-list');
    populateCountryList('countries');
});
